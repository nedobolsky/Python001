# Задача №6
"""Вы пользуетесь общественным транспортом? Вероятно, вы расплачивались за
проезд и получали билет с номером. Счастливым билетом называют такой билет
с шестизначным номером, где сумма первых трех цифр равна сумме последних трех.
 Т.е. билет с номером 385916 – счастливый, т.к. 3+8+5=9+1+6. Вам требуется
 написать программу, которая проверяет счастливость билета."""

numberTicket = int(input("Введите номер билета, состоящий из 6 цифр: "))
count = 0
firstThree = 0
secontThree = 0
lenNumberTicket = len(str(numberTicket)) # Переменная нужна для дальнейшего использования, и не должна уменьшатся
if lenNumberTicket == 6: # Проверка количества чисел в номере билета
    while count < lenNumberTicket / 2: # Суммирую первые три числа
        temp = numberTicket % 10
        secontThree += temp
        numberTicket //= 10
        count += 1
    while count < lenNumberTicket: # Суммирую оставшиеся три числа
        tmp = numberTicket % 10
        firstThree += tmp
        numberTicket //= 10
        count += 1
    if firstThree == secontThree:
        print("Счастливый билет")
    else:
        print("Обычный билетик!")
else:
    print("Вы ввели число не подходящее по условию задачи")

