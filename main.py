"""Требуется определить, можно ли от шоколадки размером n × m долек отломить k долек,
 если разрешается сделать один разлом по прямой между дольками (то есть разломить 
 шоколадку на два прямоугольника)."""

lengthRectangle = int(input("Введите длину прямоугольника: "))
widthRectangle = int(input("Введите ширину прямоугольника: "))
numberSlice = int(input("Введите количество долек, которые хотите получить при разломе шоколадки: "))

if (numberSlice % lengthRectangle == 0) or (numberSlice % widthRectangle == 0):
    print("Возможно получить количество долек при разломе")
else:
    print("Нет возможности разломить так шоколадку")
