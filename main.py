"""Найдите сумму цифр трехзначного числа."""

userNumber = int(input("Введите трехзначное число: "))
result = 0
if len(str(userNumber)) == 3:
    while userNumber > 0:
        temp = userNumber % 10
        result += temp
        userNumber //= 10
    print(result)
else:
    print("Вы ввели число не подходящее по условию задачи ")

